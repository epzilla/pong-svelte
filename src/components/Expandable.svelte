<script>
  export let defaultCollapsed = true;
  export let forceCollapsed = false;
  export let centered = false;
  export let small = false;
  export let id = '';
  export let title = '';
  export let toggle;
  let collapsed =
    forceCollapsed === true || forceCollapsed === false
      ? forceCollapsed
      : !!defaultCollapsed;
</script>

<div class="expandable" class:collapsed class:small class:centered>
  <div class="expandable-header">
    <h3><label for={`expandable-${title}`}>{title}</label></h3>
  </div>
  <input
    class="hidden-checkbox"
    checked={forceCollapsed === true || forceCollapsed === false
      ? !forceCollapsed
      : !collapsed}
    type="checkbox"
    id={`expandable-${title}`}
    on:change={() => {
      collapsed = !collapsed;
      if (toggle) {
        toggle(id);
      }
    }}
  />
  <div class="expandable-body">
    <slot />
  </div>
  <label class="toggle-btn-label" for={`expandable-${title}`} />
</div>

